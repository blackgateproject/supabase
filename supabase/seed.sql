  sessions.ip,
  sessions.tag
from
  auth.sessions;

-- create a view to read list of approved users, did included [WIP::]
create view public.approved_users as
select
  u.id,
  u.instance_id,
  r.did_str,
  r.request_status,
  r.created_at as request_created_at,
  r.network_info,
  r.updated_at as request_updated_at,
  r.verifiable_cred,
  u.created_at,
  u.updated_at,
  u.confirmed_at,
  u.invited_at,
  u.deleted_at,
  u.confirmation_token,
  u.confirmation_sent_at,
  u.recovery_token,
  u.recovery_sent_at,
  u.last_sign_in_at,
  u.raw_app_meta_data,
  u.raw_user_meta_data,
  u.is_super_admin,
  u.banned_until,
  u.reauthentication_token,
  u.reauthentication_sent_at,
  u.is_sso_user,
  u.is_anonymous
from
  auth.users u
  join requests r on jsonb_extract_path_text(u.raw_user_meta_data, variadic array['did'::text]) = r.did_str
where
  r.request_status = 'approved'::text;